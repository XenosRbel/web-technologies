<script>
    function validateEmail(e) {
        const re = /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
        console.log(re.test(String(e.value).toLowerCase()))
        if(!re.test(String(e.value).toLowerCase())){
            document.getElementById('email-validate').style.display='block';
        }else{
            document.getElementById('email-validate').style.display='none';
        }
        return re.test(String(e.value).toLowerCase());
    }
</script>
